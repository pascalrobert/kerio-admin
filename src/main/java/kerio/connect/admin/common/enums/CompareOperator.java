package kerio.connect.admin.common.enums;

public enum CompareOperator {

  Eq,         ///< '='  - equal
  NotEq,      ///< '<>' - not equal
  LessThan,   ///< '<'  - lower that
  GreaterThan,///< '>'  - greater that
  LessEq,     ///< '<=' - lower or equal
  GreaterEq,  ///< '>=' - greater or equal
  Like     ///< contains substring, % is wild character

}
