package kerio.connect.admin.common;

public class TimeRangeGroup {

  String id;
  String name;
  
  public TimeRangeGroup() {
    
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

}
