package kerio.connect.admin.common;

public class OptionalLong {

  boolean enabled;
  String value;
  
  public OptionalLong() {
    
  }

  public boolean isEnabled() {
    return enabled;
  }

  public void setEnabled(boolean enabled) {
    this.enabled = enabled;
  }

  public String getValue() {
    return value;
  }

  public void setValue(String value) {
    this.value = value;
  }

}
