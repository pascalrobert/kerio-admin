package kerio.connect.admin.users;

public class UserVoiceSettings {

  String  name;
  String  email;
  
  public UserVoiceSettings() {
    
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getEmail() {
    return email;
  }

  public void setEmail(String email) {
    this.email = email;
  }
  
}
